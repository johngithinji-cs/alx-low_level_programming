#include <stdio.h>
/**
 * main - print all single digit numbers of base 10 starting from 0
 * Description: using the main function
 * this program prints all single digit numbers of base 10 starting from 0
 * Return: 0
 */
int main(void)
{
int x;
for (x = 0; x < 10; x++)
{
	printf("%i", x);
}
printf("\n");
return (0);
}
